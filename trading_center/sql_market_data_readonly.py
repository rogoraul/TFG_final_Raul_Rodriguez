def decide_result(issues: list[dict[str, Any]], ohlc: pd.DataFrame) -> str:
    if any(row["severity"] == "high" for row in issues):
        return "sql_market_data_readonly_blocked_by_safety"
    if ohlc.empty:
        return "sql_market_data_readonly_no_rows"
    return "sql_market_data_readonly_ready_for_market_radar"


def issue(issue_id: str, severity: str, status: str, description: str, mitigation: str) -> dict[str, Any]:
    return {
        "issue_id": issue_id,
        "severity": severity,
        "status": status,
        "description": description,
        "mitigation": mitigation,
    }


def sanitize_config_source(config_source: str) -> str:
    if not config_source:
        return "not_available"
    parts = []
    for item in str(config_source).split(";"):
        key = item.split(":", 1)[0].strip()
        source = item.split(":", 1)[1].strip() if ":" in item else "configured"
        parts.append(f"{key}:{source}")
    return "; ".join(parts)


def is_local_host(host: str) -> bool:
    return host.strip().lower() in LOCAL_HOSTS


def timestamp_text(value: Any) -> str:
    if value in (None, ""):
        return ""
    if pd.isna(value):
        return ""
    if isinstance(value, pd.Timestamp):
        return value.isoformat()
    return str(value)


def utc_now() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat().replace("+00:00", "Z")


def parse_csv_tuple(value: str | None, default: tuple[str, ...]) -> tuple[str, ...]:
    if value is None:
        return default
    return tuple(item.strip() for item in value.split(",") if item.strip())


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Extract read-only SQL OHLC artifacts for the Trading Center market radar.")
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_OUTPUT_DIR)
    parser.add_argument("--timeframes", default=",".join(DEFAULT_TIMEFRAMES))
    parser.add_argument("--groups", default="")
    parser.add_argument("--bars-per-pair", type=int, default=DEFAULT_BARS_PER_PAIR)
    parser.add_argument("--allow-nonlocal-db", action="store_true")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    extract_sql_market_data(
        SqlMarketDataReadonlyConfig(
            output_dir=args.output_dir,
            timeframes=parse_csv_tuple(args.timeframes, DEFAULT_TIMEFRAMES),
            groups=parse_csv_tuple(args.groups, DEFAULT_GROUPS),
            bars_per_pair=args.bars_per_pair,
            allow_nonlocal_db=args.allow_nonlocal_db,
        )
    )


if __name__ == "__main__":
    main()
